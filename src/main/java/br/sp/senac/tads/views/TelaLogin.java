    private boolean validaCamposVazios() {
        
        boolean status = true;
        
        if (this.txtUsuario.getText().equals("") || this.txtSenha.getText().equals("")) {
            
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
            
            status = false;
            
        } else {
            
            status = true;

        }
        
        return status;
        
    }
    
    private void limpaCampos() {
        
        txtUsuario.setText("");
        txtSenha.setText("");
        
    }
    
